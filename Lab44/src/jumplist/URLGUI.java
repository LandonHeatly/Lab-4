    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        if(txtDisplay.getText().length() == 0)
            return;
        try{
            option option = new option(txtDisplay.getText(),txtImage.getText());
            txtDoc.insert("\n" + option.getHTMLText(), txtDoc.getText().indexOf("</body>")-1);
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Unable to Add Image - missing </body>");
        }
    }//GEN-LAST:event_btnAddActionPerformed

